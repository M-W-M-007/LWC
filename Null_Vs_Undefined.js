// Equality comparison
// normal comparision
// strict comparison


//1. Normal comparision (==) || only compare value
console.log(3=="3")                         //true
console.log(3==3)                           //true
console.log("nikhil" == "nikhil")           //true
console.log("nikhil" == "nikhil")           //true 

//2. strict comparision (===)
//compare value+data type

console.log(3===3)                          //true

/// Null vs Undefined

